    #[test]
    fn test_calc_lonlat() {
        let nav = GMSNavigation::new(
            ATIT,
            ORBT,
            0.000035,
            0.00002397,
            5534.5,
            6688.5,
            49165.35520833333,
            99.8822489439248,
            4.0,
            VMIS,
            ELMIS,
        );
        let (lat, lon) = nav.image_to_geographic(5000., 5000.);
        // println!("Latitude: {}, Longitude: {}", lat, lon);
        assert!((lat - 5.483019312638647).abs() < 1e-7);
        assert!((lon - 126.01089877091549).abs() < 1e-7);
    }

    #[test]
    fn test_lonlat_lineele_conv() {
        let nav = GMSNavigation::new(
            ATIT,
            ORBT,
            0.000035,
            0.00002397,
            5534.5,
            6688.5,
            49165.35520833333,
            99.8822489439248,
            4.0,
            VMIS,
            ELMIS,
        );
        let (lat, lon) = nav.image_to_geographic(5000., 5000.);
        let (line, ele) = nav.geographic_to_image_at_time(lon, lat, 49165.35520833333);
        println!("Approx Line: {}, Ele: {}", line, ele);
        let (line, ele) = nav.geographic_to_image(lon, lat);
        println!("Line: {}, Ele: {}", line, ele);
        assert!((line - 5000.0).abs() < 0.1);
        assert!((ele - 5000.0).abs() < 0.1);
    }
